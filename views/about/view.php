            <h2 class="mb-4">Qisqacha tarix</h2>
             <p style="font-size : 20px; text-align : justify;">
             120-Maktabgacha taʼlim tashkiloti 1977 yilda o'z faoliyatini boshlagan.
Olmazor tumani “Xislat” maxallasi,
Nozimaxonim 7 tor ko'chasida joylashgan.
12 ta gurux  faoliyat yuritadi.
MTT yo'nalishi  «Musiqa va sanʼat».
Mashg'ulotlar o'zbek va rus tillarida olib boriladi.
Ro'yxat bo'yicha  360 nafar tarbiyalanuvchi. 
MTT  raxbari: Sulaymanova Ziyoda Masudovna.
MTTda 18 nafar pedagog xodimlar va 26 nafar xodimlar faoliyat yuritadi.
2018 yil   tashkilot «Obod maxalla» dasturi asosida kapital taʼmirdan chiqdi.
2019 yil  4 yanvarda  kapital taʼmirdan so'ng ish faoliyatini boshladi.
MTTda mashg'ulotlar «Ilk qadam» Davlat dasturi asosida olib boriladi.
