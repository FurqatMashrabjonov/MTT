            <h2 class="mb-4">Tadbirlar</h2>
Tumanda har yili «Istiqlol yulduzchalari» ko'rik tanlovi o'tqaziladi va tashkilotimiz  tarbiyalanuvchilari  uz namoishlarini taqdim etadilar.
MTT muntazam ravishda  tarbiyachilar tomonidan «Ochiq mashg'ulot» lar olib boriladi.
Tashkilotimiz  Puchon  Universiteti bilan xamkorlikda ish olib boradi. Puchon universiteti talabalari  amaliyot o'tash uchun keladilar.
Yillik reja asosida  Bayram tadbirlari  «Mustaqillik bayrami», «Ustozlar va murabbiylar» bayrami, “Mexrjon” bayrami, «Til bayrami», «Konstitutsiya bayrami», «Yangi yil mujizalari» bayrami, «14 yanvar Xarbiy va vatanparvarlik» bayrami, «8-mart bayrami», 21-mart “Navruz” bayrami, «Xayr bog'cha-salom maktab» bayramlari   o'tqazib  boriladi.</p>
